public class Carta {
    private String nome;
    private int custo;
    private int efeito;

    // Getters

    public String getNome(){
        return nome;
    }

    public int getCusto(){
        return custo;
    }

    public int getEfeito(){
        return efeito;
    }

    // Setters

    public void setNome(String nome){
        this.nome = nome;
    }

    public void setCusto(int custo){
        this.custo = custo;
    }

    public void setEfeito(int efeito){
        this.efeito = efeito;
    }

    
}
